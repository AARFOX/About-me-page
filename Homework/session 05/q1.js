// Declare variables using let (can change) and const (cannot change)
const name = "Mohamed Ahmed";      // String - your name
let age = 22;                      // Number - your age  
let city = "Cairo";               // String - your city
let isStudent = true;             // Boolean - are you a student?

// Log each variable
console.log("========== Profile Card ==========");
console.log("Name:", name);
console.log("Age:", age);
console.log("City:", city);
console.log("Is Student:", isStudent);

// Log the typeof each variable
console.log("\n========== Data Types ==========");
console.log("Type of name:", typeof name);           // string
console.log("Type of age:", typeof age);             // number
console.log("Type of city:", typeof city);           // string
console.log("Type of isStudent:", typeof isStudent); // boolean

// ============ Expected Output ============
// Name: Mohamed Ahmed
// Age: 22
// City: Cairo
// Is Student: true
// 
// Type of name: string
// Type of age: number
// Type of city: string
// Type of isStudent: boolean
